package proyectopoo;

public class Roles {
    private String nombre;
    private String descripcion;

    public Roles(String nombre, String descripcion) {
        this.nombre = nombre;
        this.descripcion = descripcion;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public String getNombre() {
        return nombre;
    }
    
    
    
}
